package JavaCollections.Generics;

public class Elephant {
    int age;
    String name;
    Boolean trunk;

    public Elephant(int age, String name){
        this.age = age;
        this.name = name;
        this.trunk = true;
    }

    public Elephant(){
        this.age = age;
        this.name = name;
        this.trunk = true;
    }
}
